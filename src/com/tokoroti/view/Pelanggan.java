    private void btnTambahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTambahActionPerformed
        
        if (txtNama.getText().equals("") || txtAlamat.getText().equals("") || 
                txtEmail.getText().equals("") || txtNoTel.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Form tidak boleh Kosong");
        } else {
            if(rdGenderL.isSelected()){
                gender = rdGenderL.getText();
            }else if(rdGenderP.isSelected()){
                gender = rdGenderP.getText();
            }
            modelPelanggan = new ModelPelanggan(txtNama.getText(), txtAlamat.getText(), gender, txtEmail.getText(), txtNoTel.getText());
            if (modelPelanggan.insertPelanggan()) {
                detailPelanggan();
                JOptionPane.showMessageDialog(this, "Tambah Pelanggan Berhasil");
            }
        }
    }//GEN-LAST:event_btnTambahActionPerformed

