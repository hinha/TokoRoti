    public void detailAdmin() {
        DefaultTableModel modelDataAdmin = new DefaultTableModel();
        modelDataAdmin.addColumn("ID");
        modelDataAdmin.addColumn("Username");
        modelDataAdmin.addColumn("Password");
        modelDataAdmin.addColumn("Alamat");
        modelDataAdmin.addColumn("Email");
        modelDataAdmin.addColumn("NoTelp");
        tblPegawai.setModel(modelDataAdmin);
        try {
            kon.res = kon.stat.executeQuery("SELECT * FROM admin");
            while(kon.res.next()){  
                modelDataAdmin.addRow(new Object[]{
                    kon.res.getInt(1),
                    kon.res.getString(2),
                    kon.res.getString(3),
                    kon.res.getString(4),
                    kon.res.getString(5),
                    kon.res.getString(6),
                });
            }
        } catch (SQLException ex) {
            Logger.getLogger(Keranjang.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    
    public void detailPelanggan() {
        DefaultTableModel modelDataPelanggan = new DefaultTableModel();
        modelDataPelanggan.addColumn("ID");
        modelDataPelanggan.addColumn("Nama");
        modelDataPelanggan.addColumn("JK");
        modelDataPelanggan.addColumn("Email");
        modelDataPelanggan.addColumn("Alamat");
        modelDataPelanggan.addColumn("NoTelp");
        tblPelanggan.setModel(modelDataPelanggan);
        try {
            kon.res = kon.stat.executeQuery("SELECT pelanggan.id_pelanggan, pelanggan.nama, pelanggan.gender, "
                    + "pelanggan.email, pelanggan.alamat, pelanggan.no_hp FROM pelanggan");
            while(kon.res.next()){  
                modelDataPelanggan.addRow(new Object[]{
                    kon.res.getInt(1),
                    kon.res.getString(2),
                    kon.res.getString(3),
                    kon.res.getString(4),
                    kon.res.getString(5),
                    kon.res.getString(6),
                });
            }
        } catch (SQLException ex) {
            Logger.getLogger(Keranjang.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    
